import Foundation

extension UIColor{
    class func mobileBlue() -> UIColor{
        return UIColor(red: 0/255, green: 148/255, blue: 213/255, alpha: 1)
    }
    
    class func lightBlue() -> UIColor{
        return UIColor(red: 150/255, green: 210/255, blue: 250/255, alpha: 1)
    }
    
    class func backgroundGray() -> UIColor{
        return UIColor(red: 240/255, green: 240/255, blue: 240/255, alpha: 1)
    }
}