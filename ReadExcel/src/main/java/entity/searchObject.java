package entity;

/**
 * @author sj
 * 
 */
public class searchObject
{
    private int id;
    private String name;
    private String pinyiing;

    /**
     * @return the id
     */
    public int getId()
    {
	return id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(int id)
    {
	this.id = id;
    }

    /**
     * @return the name
     */
    public String getName()
    {
	return name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(String name)
    {
	this.name = name;
    }

    /**
     * @return the pinyiing
     */
    public String getPinyiing()
    {
	return pinyiing;
    }

    /**
     * @param pinyiing
     *            the pinyiing to set
     */
    public void setPinyiing(String pinyiing)
    {
	this.pinyiing = pinyiing;
    }
}
