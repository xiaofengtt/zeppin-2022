
package enfo.crm.vo;


public class TaskInfoVO {
	private Integer op_code;
	private Integer read_flag;
	private Integer product_id;
	private Integer read_flag1;

	
	/**
	 * @return 返回 op_code。
	 */
	public Integer getOp_code() {
		return op_code;
	}
	/**
	 * @param op_code 要设置的 op_code。
	 */
	public void setOp_code(Integer op_code) {
		this.op_code = op_code;
	}
	/**
	 * @return 返回 product_id。
	 */
	public Integer getProduct_id() {
		return product_id;
	}
	/**
	 * @param product_id 要设置的 product_id。
	 */
	public void setProduct_id(Integer product_id) {
		this.product_id = product_id;
	}
	/**
	 * @return 返回 read_flag。
	 */
	public Integer getRead_flag() {
		return read_flag;
	}
	/**
	 * @param read_flag 要设置的 read_flag。
	 */
	public void setRead_flag(Integer read_flag) {
		this.read_flag = read_flag;
	}
	/**
	 * @return 返回 read_flag1。
	 */
	public Integer getRead_flag1() {
		return read_flag1;
	}
	/**
	 * @param read_flag1 要设置的 read_flag1。
	 */
	public void setRead_flag1(Integer read_flag1) {
		this.read_flag1 = read_flag1;
	}
}
