import UIKit
extension UIColor{
    class func mainColor() -> UIColor{
        return UIColor(red: 13/255, green: 169/255, blue: 69/255, alpha: 1)
    }
    class func buttonGray() -> UIColor{
        return UIColor(red: 201/255, green: 202/255, blue: 202/255, alpha: 1)
    }
    class func fontGray() -> UIColor{
        return UIColor(red: 139/255, green: 139/255, blue: 139/255, alpha: 1)
    }
    class func backgroundGray() -> UIColor{
        return UIColor(red: 245/255, green: 245/255, blue: 245/255, alpha: 1)
    }
    class func spaceLineGray() -> UIColor{
        return UIColor(red: 230/255, green: 230/255, blue: 230/255, alpha: 1)
    }
    class func buttonBlue() -> UIColor{
        return UIColor(red: 86/255, green: 194/255, blue: 251/255, alpha: 1)
    }
    class func labelRed() -> UIColor{
        return UIColor(red: 240/255, green: 70/255, blue: 70/255, alpha: 1)
    }
    class func transparentColor() -> UIColor{
        return UIColor(red: 1, green: 1, blue: 1, alpha: 0)
    }
    class func defultBlue() -> UIColor{
        return UIColor(red: 0, green: 122/255, blue: 1, alpha: 1)
    }
}
 