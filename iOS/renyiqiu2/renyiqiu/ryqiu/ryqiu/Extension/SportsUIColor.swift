import Foundation
extension UIColor{
    class func colorMainColor() -> UIColor{
        return UIColor(red: 250/255, green: 40/255, blue: 0/255, alpha: 1)
    }
    class func colorFontBlack() -> UIColor{
        return UIColor(red: 52/255, green: 52/255, blue: 52/255, alpha: 1)
    }
    class func colorFontDarkGray() -> UIColor{
        return UIColor(red: 109/255, green: 108/255, blue: 104/255, alpha: 1)
    }
    class func colorFontGray() -> UIColor{
        return UIColor(red: 172/255, green: 172/255, blue: 172/255, alpha: 1)
    }
    class func colorBgGray() -> UIColor{
        return UIColor(red: 243/255, green: 243/255, blue: 243/255, alpha: 1)
    }
    class func ColorPlaceholder() -> UIColor{
        return UIColor(red: 172/255, green: 172/255, blue: 172/255, alpha: 1)
    }
}
