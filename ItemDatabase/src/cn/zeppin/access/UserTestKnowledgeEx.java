package cn.zeppin.access;

public class UserTestKnowledgeEx {
	private int knowledgeId;
	private String knowledgeName;
	private int okCount;
	private int totalCount;
	private float precent;
	private String strPrecent;
	
	public UserTestKnowledgeEx(){}

	public int getKnowledgeId() {
		return knowledgeId;
	}

	public void setKnowledgeId(int knowledgeId) {
		this.knowledgeId = knowledgeId;
	}

	public String getKnowledgeName() {
		return knowledgeName;
	}

	public void setKnowledgeName(String knowledgeName) {
		this.knowledgeName = knowledgeName;
	}

	public int getOkCount() {
		return okCount;
	}

	public void setOkCount(int okCount) {
		this.okCount = okCount;
	}

	public int getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}

	public float getPrecent() {
		return precent;
	}

	public void setPrecent(float precent) {
		this.precent = precent;
	}

	public String getStrPrecent() {
		return strPrecent;
	}

	public void setStrPrecent(String strPrecent) {
		this.strPrecent = strPrecent;
	}
	
	
	
}
